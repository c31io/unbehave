			<h1>The Science of Behavioral Change</h1>

			<p class="lead">
				Understanding the psychology behind addiction and behavioral patterns can help you make
				better decisions when faced with temptation.
			</p>

			<h2>The Regret Aversion Principle</h2>

			<p>
				Research in behavioral economics has shown that humans are remarkably influenced by past
				regrets. The <strong>regret aversion principle</strong> suggests that people often make
				decisions to avoid future regret rather than to maximize pleasure.
			</p>

			<blockquote>
				<p>
					"The pain of regret is often more powerful than the anticipation of pleasure." — Daniel
					Kahneman, Nobel Prize Winner in Economics
				</p>
			</blockquote>

			<p>
				This app leverages this principle by making your past regrets immediately accessible when
				you're about to make a decision you might later regret.
			</p>

			<h2>The Dopamine Loop</h2>

			<p>
				Modern technology exploits our brain's reward system. Every notification, like, or infinite
				scroll triggers a dopamine release, creating a powerful feedback loop:
			</p>

			<ol>
				<li><strong>Trigger</strong> — You feel bored, anxious, or seeking validation</li>
				<li><strong>Action</strong> — You open the app or engage in the behavior</li>
				<li><strong>Reward</strong> — Brief dopamine hit from novelty/validation</li>
				<li><strong>Craving</strong> — Brain wants more, loop repeats</li>
			</ol>

			<p>
				The problem? This loop is designed to be addictive. Companies employ psychologists and use
				A/B testing to maximize engagement, which often means maximizing your time spent (and your
				regret later).
			</p>

			<h2>Implementation Intentions</h2>

			<p>
				Research by psychologist Peter Gollwitzer shows that <em>implementation intentions</em> — specific
				if-then plans — dramatically increase success rates for behavioral change.
			</p>

			<p>Instead of vague goals like "I'll use social media less," try:</p>

			<ul>
				<li>
					<strong>IF</strong> I feel the urge to check social media, <strong>THEN</strong> I'll open
					Unbehave and review my regrets
				</li>
				<li>
					<strong>IF</strong> I'm tempted to play games all day, <strong>THEN</strong> I'll read my
					past regrets first
				</li>
			</ul>

			<p>This app serves as your implementation intention tool.</p>

			<h2>The Mere Exposure Effect</h2>

			<p>
				Repeated exposure to your regrets creates a psychological association between the temptation
				and the negative outcomes. Over time, this can rewire your automatic responses.
			</p>

			<p>
				Studies show it takes 30-90 days to form new habits. Consistency is key — even small
				victories add up.
			</p>

			<h2>Cognitive Behavioral Techniques</h2>

			<p>
				This app incorporates elements of <strong>Cognitive Behavioral Therapy (CBT)</strong>, which
				is proven effective for addiction and impulse control:
			</p>

			<ul>
				<li>
					<strong>Awareness</strong> — Tracking temptations makes you conscious of patterns
				</li>
				<li>
					<strong>Reflection</strong> — Writing regrets helps process emotions and consequences
				</li>
				<li>
					<strong>Intervention</strong> — Pausing before acting disrupts automatic behaviors
				</li>
			</ul>

			<h2>Why This Works</h2>

			<p>Traditional willpower alone often fails because:</p>

			<ol>
				<li>Willpower is a limited resource (ego depletion)</li>
				<li>Future consequences feel abstract and distant</li>
				<li>The dopamine hit is immediate and tangible</li>
			</ol>

			<p>
				By making past regrets <strong>immediate</strong> and <strong>visceral</strong>, this app
				helps balance the equation. Your brain gets a competing emotional signal that can override
				the craving.
			</p>

